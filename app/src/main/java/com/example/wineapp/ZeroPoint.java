package com.example.wineapp;

public class ZeroPoint {
    private int xZeroPoint;
    private int yZeroPoint;

    public ZeroPoint(int x, int y){
        xZeroPoint=x;
        yZeroPoint=y;
    }

    public int getxZeroPoint() {
        return xZeroPoint;
    }

    public int getyZeroPoint() {
        return yZeroPoint;
    }
}
